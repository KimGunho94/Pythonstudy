'''
파일명: Ex04-1-arithmetic.py

산술(Arithmetic)
    숫자 계산을 할 떄 사용 하는 연산자
'''
a = 7
b = 2
# 덧셈
print('{} + {} = {}'.format(a, b, a + b))
# 뺄셈
print('{} - {} = {}'.format(a, b, a - b))
# 곱셈
print('{} * {} = {}'.format(a, b, a * b))
# 거듭 제곱
print('{} ** {} = {}'.format(a, b, a ** b))
# 나누기
print('{} / {} = {}'.format(a, b, a / b))
# 몫
print('{} // {} = {}'.format(a, b, a // b))
# 나머지
print('{} % {} = {}'.format(a, b, a % b))
